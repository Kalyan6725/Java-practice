list =["apple", "banana", "cherry"]
print(list)
print(type(list))
print(list[0]) #accessing first element 
print("apple" in list) #checking if apple is in list
print(list[0:2]) #slicing list
print(list[-1]) #accessing last element
#access from last to first
print(list[::-1]) #reversing list
print(list[1:]) #accessing elements from index 1 to end
print(list[:2]) #accessing elements from start to index 2
print("Length of list:", len(list)) #length of list
print()